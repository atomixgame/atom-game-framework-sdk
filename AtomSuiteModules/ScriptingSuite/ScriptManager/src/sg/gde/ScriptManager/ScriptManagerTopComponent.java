    private boolean checkOpenedProject() {
        //Lookup utilLookup = Utilities.actionsGlobalContext();
        //proj = utilLookup.lookup(Project.class);
        Project[] openProjects = OpenProjects.getDefault().getOpenProjects();
        if (OpenProjects.getDefault().getMainProject() != null) {
            proj = OpenProjects.getDefault().getMainProject();
            return true;
        } else if (openProjects != null & openProjects.length != 0) {

            proj = openProjects[0];
            return true;
        } else {
            return false;
        }

    }
