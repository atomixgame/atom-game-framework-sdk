    public void startScriptEngineInProject() {
        FileObject root = proj.getProjectDirectory();
        String path = root.getPath();
        Logger.getLogger("ScriptEngine").info("The script path :" + path);
        scriptEngine = new NetbeanGroovyScriptEngine();
        scriptEngine.startEngine(path + "/src/script/");
    }
