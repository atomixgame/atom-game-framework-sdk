private void btnRunScriptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunScriptActionPerformed
    // TODO add your handling code here:
    if (scriptEngine != null) {
        String text = txtCode.getText();
        if (!text.trim().equals("")) {
            lblStatus.setText("Status: Running ...");
            scriptEngine.eval(text);
            //txtOutput.setText(scriptEngine.getAllCodeLines());
            lblStatus.setText("Status: Finished!");
        } else {
            lblStatus.setText("Error: You have to enter code !");
        }
    } else {
        lblStatus.setText("Error: Start Script Engine first. Click 'Active' Button !");
    }
}//GEN-LAST:event_btnRunScriptActionPerformed

